# Boolean expressions examples
print(10 < 100)        # True
print(100 != 100)      # False
print(50 >= 50)        # True

# Using a variable
age = 25
print(age < 18)        # False
print(age < 21)        # False
print(age < 31)        # True

# String comparisons
print("a" < "b")       # True
print("b" < "a")       # False
print("John" < "Terry")# True
print("john" < "Terry")# False, lowercase > uppercase
